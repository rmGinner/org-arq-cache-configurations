public class CacheConfiguration {

    private Integer tag;

    private Integer line;

    private Integer word;

    private Integer wordByte;

    private Integer cacheLines;

    private Integer cacheLineWords;

    public Integer getTag() {
        return tag;
    }

    public void setTag(Integer tag) {
        this.tag = tag;
    }

    public Integer getLine() {
        return line;
    }

    public void setLine(Integer line) {
        this.line = line;
    }

    public Integer getWord() {
        return word;
    }

    public void setWord(Integer word) {
        this.word = word;
    }

    public Integer getWordByte() {
        return wordByte;
    }

    public void setWordByte(Integer wordByte) {
        this.wordByte = wordByte;
    }

    public Integer getCacheLines() {
        return cacheLines;
    }

    public void setCacheLines(Integer cacheLines) {
        this.cacheLines = cacheLines;
    }

    public Integer getCacheLineWords() {
        return cacheLineWords;
    }

    public void setCacheLineWords(Integer cacheLineWords) {
        this.cacheLineWords = cacheLineWords;
    }
}
